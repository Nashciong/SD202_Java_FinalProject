    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        // TODO add your handling code here:
        try {
            String barcode = jCode.getText();
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/sport_scheduler", "root", "");
            pst = con.prepareStatement("select * from sports where barcode=?");

            pst.setString(1, barcode);
            rs = pst.executeQuery();

            if (barcode.equals("")) {
                JOptionPane.showMessageDialog(this, "Please fill the required field!", "Input Something!", JOptionPane.ERROR_MESSAGE);

            } else if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "Barcode doesn't Exist!", "Input Correctly!", JOptionPane.ERROR_MESSAGE);
                jCode.setText("");
            } else {
                String stat = rs.getString("status");

                JOptionPane.showMessageDialog(null, "Your schedule status is: " + stat.toUpperCase(), "Status!", JOptionPane.PLAIN_MESSAGE);

            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CreateSchedule.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

