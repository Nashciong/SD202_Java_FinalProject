    private void jComboBox1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jComboBox1KeyReleased
        // TODO add your handling code here:

        if (jComboBox1.getText().isEmpty()) {
            timeEnd.setText(null);
            jAmount.setText(null);
        } else {
            String jBox1 = (String) jComboBox2.getSelectedItem();
            String jBox2 = (String) jComboBox3.getSelectedItem();
            int fixprice_perHour = 350;
            int tmstrt = Integer.parseInt(timeStart.getText());
            int hr = Integer.parseInt(jComboBox1.getText());
            int payable = fixprice_perHour * hr;
            int totalHour = hr + tmstrt;

            if (totalHour == 12 && "AM".equals(jBox1)) {
                jAmount.setText(Integer.toString(payable));
                timeEnd.setText(Integer.toString(totalHour));
                jComboBox3.setSelectedIndex(1);
            } else if (totalHour == 12 && "PM".equals(jBox1)) {
                jAmount.setText(Integer.toString(payable));
                timeEnd.setText(Integer.toString(totalHour));
                jComboBox3.setSelectedIndex(0);
            } else if (totalHour <= 12 && "AM".equals(jBox1)) {
                jAmount.setText(Integer.toString(payable));
                timeEnd.setText(Integer.toString(totalHour));
                jComboBox3.setSelectedItem(jComboBox2.getSelectedItem().toString());
            } else if (totalHour <= 12 && "PM".equals(jBox1)) {
                jAmount.setText(Integer.toString(payable));
                timeEnd.setText(Integer.toString(totalHour));
                jComboBox3.setSelectedItem(jComboBox2.getSelectedItem().toString());
            } else if (totalHour >= 12 && "AM".equals(jBox1)) {
                int res = totalHour - 12;
                jAmount.setText(Integer.toString(payable));
                timeEnd.setText(Integer.toString(res));
                jComboBox3.setSelectedIndex(1);
            } else if (totalHour > 12 && "PM".equals(jBox1)) {
                jAmount.setText(Integer.toString(payable));
                int res = totalHour - 12;
                timeEnd.setText(Integer.toString(res));
                jComboBox3.setSelectedIndex(0);
            }
        }

    }//GEN-LAST:event_jComboBox1KeyReleased

